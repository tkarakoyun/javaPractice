package day18;

public class WhileLoopReview {

    public static void main(String[] args) {

        int x = 10;

        while (x> 5){
            System.out.println(x + " is more than 5");
            --x;
        }
        System.out.println("The End");

        int y = 50;

        while (y>0){
            System.out.println(y + " is more than 5");
            --y;
        }
        System.out.println("The End");


    }
}
