package day07;

public class RelationalOperator_Variable {

    public static void main(String[] args) {

        // Relation operator is used to compare 2 oprands
        // and return true or false
        // anything action or expression
        // // than return true or false result
        //can be called boolean expression

        int myNumber = 10;

        System.out.println(myNumber > 5);
        System.out.println(myNumber < -19);
        System.out.println(myNumber >= 5);
        System.out.println(myNumber >= 8);
        System.out.println(myNumber <= 5);
        System.out.println(myNumber >= 8);

        // == is used to check for equality

        // and VERY different from single =

        // different between == and = same as different between equal ad equality in daily English

        System.out.println(myNumber == 5);

        // != is used to check for inequality

        System.out.println(myNumber != 10);
        System.out.println(myNumber != 6);


        // == and != are questions. These signs are used to check for equality or inequality.
    }

}